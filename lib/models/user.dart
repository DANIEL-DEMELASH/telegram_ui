  final String id;
  final String name;
  final String imageUrl;
  final String phoneNumber;
  final String username;
  final String bio;
  final String msg;
  final String lastSeen;

  User( 
      {required this.id,
      required this.name,
      required this.imageUrl,
      required this.phoneNumber,
      required this.username,
      required this.bio,
      required this.msg,
      required this.lastSeen});
}
